def main():
    
    speed=70
    time1=6
    time2=10
    time3=15

    # calculate distance=speed*time
    # car speed=70mph/h
    distance1=speed*time1
    distance2=speed*time2
    distance3=speed*time3

    # display distance in 6 hr/10hr/15hr
    print('Distance the car will travel in 6 hours',distance1,'miles')
    print('Distance the car will travel in 10 hours',distance2,'miles')
    print('Distance the car will travel in 15 hours',distance3,'miles')

main()
